import { WsIoAdapter } from '@/infrastructure/ws-adapter/ws-adapter';
import fastifyCookie from '@fastify/cookie';
import fastifyHelmet from '@fastify/helmet';
import fastifySession from '@fastify/session';
import {
  ClassSerializerInterceptor,
  INestApplication,
} from '@nestjs/common';
import { Reflector } from '@nestjs/core';
import { NestFastifyApplication } from '@nestjs/platform-fastify';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { apiReference } from '@scalar/nestjs-api-reference';
import compression from 'compression';
import { I18nValidationExceptionFilter, I18nValidationPipe } from 'nestjs-i18n';
import { ResponseInterceptor } from '../application/http/common/interceptors/response.interceptor';
import { envConfig } from './env.config';
import { createSessionConfig } from './session.config';

export class AppConfig {
  static async setup(app: INestApplication & NestFastifyApplication) {
    const wsIoAdapter = new WsIoAdapter(app);
    await wsIoAdapter.connectToRedis();
    app.useWebSocketAdapter(wsIoAdapter);

    // Compression
    app.use(compression({
      filter: () => { return true; },
      threshold: 0
    }));

    // CORS
    app.enableCors({
      origin: (origin, callback) => {
        const allowedOrigins = process.env.CORS_ORIGIN?.split(',') || [];

        if (!origin ||
          origin === 'null' ||
          origin.includes('localhost') ||
          origin.includes('127.0.0.1') ||
          /^http:\/\/192\.168\.\d{1,3}\.\d{1,3}(:\d+)?$/.test(origin) ||
          /^http:\/\/10\.\d{1,3}\.\d{1,3}\.\d{1,3}(:\d+)?$/.test(origin) ||
          /^http:\/\/172\.(1[6-9]|2\d|3[0-1])\.\d{1,3}\.\d{1,3}(:\d+)?$/.test(origin) ||
          allowedOrigins.includes(origin)) {
          callback(null, true);
        } else if (allowedOrigins.length === 0 || allowedOrigins[0] === '*') {
          callback(null, true);
        } else {
          callback(new Error('Not allowed by CORS'));
        }
      },
      methods: 'GET,HEAD,PUT,PATCH,POST,DELETE',
      allowedHeaders: 'Content-Type, Accept, Authorization',
      credentials: true,
    });

    await app.register(fastifyCookie);
    await app.register(fastifySession, await createSessionConfig());

    app.useGlobalPipes(
      new I18nValidationPipe({
        transform: true,
        whitelist: true,
        forbidNonWhitelisted: true,
        transformOptions: {
          enableImplicitConversion: true,
        },
      }),
    );

    app.useGlobalFilters(
      new I18nValidationExceptionFilter({
        detailedErrors: true,
      }),
    );

    app.useGlobalInterceptors(
      new ResponseInterceptor(),
      new ClassSerializerInterceptor(app.get(Reflector)),
    );

    // Helmet
    const cspConfig = envConfig.isProduction ? {
      contentSecurityPolicy: {
        directives: {
          defaultSrc: ["'self'"],
          scriptSrc: ["'self'", "'unsafe-inline'", 'https://cdn.jsdelivr.net'],
          scriptSrcAttr: ["'none'"],
          styleSrc: [
            "'self'",
            "'unsafe-inline'",
            'https://cdn.jsdelivr.net',
            'https://fonts.googleapis.com',
          ],
          styleSrcElem: [
            "'self'",
            'https://cdn.jsdelivr.net',
            'https://fonts.googleapis.com',
            "'unsafe-inline'",
          ],
          imgSrc: ["'self'", 'data:', 'https:'],
          connectSrc: ["'self'"],
          fontSrc: [
            "'self'",
            'https://cdn.jsdelivr.net',
            'https://fonts.gstatic.com',
            'data:',
          ],
          objectSrc: ["'none'"],
          mediaSrc: ["'self'"],
          frameSrc: ["'none'"],
        },
      },
    } : {
      contentSecurityPolicy: {
        directives: {
          defaultSrc: ["'self'"],
          scriptSrc: ["'self'", "'unsafe-inline'", 'https://cdn.jsdelivr.net'],
          scriptSrcAttr: ["'unsafe-inline'"],
          styleSrc: [
            "'self'",
            "'unsafe-inline'",
            'https://cdn.jsdelivr.net',
            'https://fonts.googleapis.com',
          ],
          styleSrcElem: [
            "'self'",
            'https://cdn.jsdelivr.net',
            'https://fonts.googleapis.com',
            "'unsafe-inline'",
          ],
          imgSrc: ["'self'", 'data:', 'https:', 'http:'],
          connectSrc: ["'self'", 'http:', 'ws:', 'wss:'],
          fontSrc: [
            "'self'",
            'https://cdn.jsdelivr.net',
            'https://fonts.gstatic.com',
            'data:',
          ],
          objectSrc: ["'none'"],
          mediaSrc: ["'self'"],
          frameSrc: ["'none'"],
          upgradeInsecureRequests: null,
        },
      },
    };

    await app.register(fastifyHelmet as any, cspConfig as any);

    this.setupSwagger(app);
  }

  private static setupSwagger(app: INestApplication & NestFastifyApplication) {
    const config = new DocumentBuilder()
      .addServer(`http://localhost:${envConfig.port}`, 'Local server')
      .setTitle('API')
      .setDescription(
        '# API Documentation\n\n' +
        'RESTful API with NestJS and Fastify.\n\n' +
        '## Features\n\n' +
        '- **Redis Cache**: Full cache support with multiple strategies\n' +
        '- **Validation**: Automatic DTO validation with class-validator\n' +
        '- **Security**: Helmet configured for common vulnerabilities protection\n' +
        '- **Performance**: Fastify for maximum performance\n\n',
      )
      .setVersion('1.0')
      .build();

    const documentFactory = SwaggerModule.createDocument(app, config);

    // Scalar UI
    app.use(
      '/docs',
      apiReference({
        content: documentFactory,
        showDeveloperTools: 'never',
        theme: 'bluePlanet',
        darkMode: true,
        withFastify: true,
        layout: 'modern',
        pageTitle: 'API Documentation',
      }),
    );

    // Swagger endpoints
    SwaggerModule.setup('swagger', app, documentFactory, {
      jsonDocumentUrl: '/swagger/json',
      yamlDocumentUrl: '/swagger/yaml',
      swaggerUiEnabled: false,
    });
  }
}
